// Generate table data for pagination
export const generateRevenueData = () => {
  const names = [
    "John Doe",
    "Jane Smith",
    "Bob Johnson",
    "Alice Williams",
    "Charlie Brown",
    "Diana Prince",
    "Edward Norton",
    "Fiona Apple",
    "George Miller",
    "Hannah Montana",
    "Ian Malcolm",
    "Julia Roberts",
    "Kevin Hart",
    "Laura Palmer",
    "Michael Scott",
    "Nancy Drew",
    "Oscar Isaac",
    "Penny Lane",
    "Quinn Fabray",
    "Rachel Green",
  ];
  const plans = [
    "Individual Monthly",
    "Individual Annual",
    "Family Monthly",
    "Family Annual",
  ];
  const statuses = ["Active", "Trial", "Cancelled", "Paused"];
  const paymentMethods = ["Credit Card", "PayPal", "Apple Pay", "Google Pay"];
  const data = [];

  for (let i = 1; i <= 35; i++) {
    const plan = plans[i % plans.length];
    const amount =
      plan === "Individual Monthly"
        ? 9.99
        : plan === "Individual Annual"
        ? 99.99
        : plan === "Family Monthly"
        ? 19.99
        : 199.99;

    data.push({
      id: String(i),
      userId: `user_${100 + i}`,
      userName: names[i % names.length],
      plan,
      amount,
      status: statuses[i % statuses.length],
      paymentMethod: paymentMethods[i % paymentMethods.length],
      discount: i % 3 === 0 ? (amount * 0.2).toFixed(2) : 0,
      billingDate: `2024-${String(Math.floor(Math.random() * 12) + 1).padStart(
        2,
        "0"
      )}-${String(Math.floor(Math.random() * 28) + 1).padStart(2, "0")}`,
      nextBilling: `2025-${String(Math.floor(Math.random() * 12) + 1).padStart(
        2,
        "0"
      )}-${String(Math.floor(Math.random() * 28) + 1).padStart(2, "0")}`,
    });
  }
  return data;
};
